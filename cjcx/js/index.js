// 页面初始化函数
function initializePage() {
    // 重置页面到默认状态
    const testSchoolDesc = document.querySelector('.test-school-desc');
    const testSchool = document.querySelector('.test-school');
    const testRegionItems = document.querySelectorAll('.test-region-list li');
    const testSchoolList = document.querySelector('.test-school-list');
    
    // 显示test-school-desc，隐藏test-school
    // if (testSchoolDesc) testSchoolDesc.style.display = 'block';
    // if (testSchool) testSchool.style.display = 'none';
    
    // 清除所有选中状态
    testRegionItems.forEach(item => {
        item.classList.remove('selected');
    });
    
    // 重置学校列表，只保留标题行
    if (testSchoolList) {
        const titleRow = testSchoolList.querySelector('li:first-child');
        testSchoolList.innerHTML = '';
        if (titleRow) {
            testSchoolList.appendChild(titleRow);
        }
    }
    
    // 清空地区名称
    const areaSpan = document.querySelector('.test-school-area');
    if (areaSpan) areaSpan.textContent = '';
}

// 获取页面元素
const ul = document.querySelector(".test-school-list");
const area = document.querySelector(".test-school-area");
const regionList = document.querySelector(".test-region-list");

// 为地区列表添加点击事件
regionList.addEventListener("click", function(e) {
  // 检查点击的是否是li元素
  if (e.target.tagName === "LI") {
    // 移除所有li的selected类
    const allRegions = regionList.querySelectorAll("li");
    for (let i = 0; i < allRegions.length; i++) {
      allRegions[i].classList.remove("selected");
    }
    
    // 为当前点击的li添加selected类
    e.target.classList.add("selected");
    
    // 获取点击的地区名称
    const selectedRegion = e.target.textContent;
    
    // 在schoolInfo数组中查找对应的地区信息
    const regionInfo = schoolInfo.find(item => item.area === selectedRegion);
    
    if (regionInfo) {
      // 隐藏test-school-desc，显示test-school
      const testSchoolDesc = document.querySelector('.test-school-desc');
      const testSchool = document.querySelector('.test-school');
      if (testSchoolDesc) testSchoolDesc.style.display = 'none';
      if (testSchool) testSchool.style.display = 'block';
      
      // 清除原有的li，只保留第一个li元素（标题行）
      const existingItems = ul.querySelectorAll("li");
      for (let i = 1; i < existingItems.length; i++) {
        existingItems[i].remove();
      }
      
      // 设置地区名称
      area.innerHTML = regionInfo.area;
      
      // 创建新的li元素
      for (let i = 0; i < regionInfo.schools.length; i++) {
        const li = document.createElement("li");
        li.innerHTML = `<span class="test-school-name" data-exam-unit="${i + 1}">${regionInfo.schools[i]}</span>`
        ul.appendChild(li);
      }
    }
  }
})

// 为学校列表添加点击事件（除了标题行）
ul.addEventListener("click", function(e) {
  // 检查点击的是否是学校名称span，且不是标题行
  if (e.target.classList.contains("test-school-name") && e.target.closest('li') !== ul.querySelector('li:first-child')) {
    // 获取被点击的学校名称
    const schoolName = e.target.textContent;
    
    // 将学校名称保存到sessionStorage，以便在search.html页面中使用
    sessionStorage.setItem('selectedSchool', schoolName);
    
    // 跳转到search.html页面
    window.location.href = 'search.html';
  }
})

// 页面加载完成后执行初始化
document.addEventListener('DOMContentLoaded', initializePage);